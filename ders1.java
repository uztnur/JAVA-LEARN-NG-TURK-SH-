package baslangic1;

public class ders1 {

	public static void main(String[] args) {
		System.out.println("HELLO");
        System.out.println("JAVA'S WORLD");
        // eğer ""içinde yazmadığımız sayılarla öperatörleri kullanırsak bunlar gerekli işlemi yapar ama "" içinde yazarsak bunu metinsel ifade olarak algıladığı için birşey yapamaz ve olduğu gibi yazar.
        System.out.println(10+15);
        System.out.println(10+"15");
        // \n println görevini görür ve kullanıldıktan sonra diğer satıra geçerek yazdırmaya başlar.
        System.out.println("Hello \n World");
        //\t ifadesi kullanıldığında tab kadar boşluk bırakır.
        System.out.println("\t Hello World");
	}

}
